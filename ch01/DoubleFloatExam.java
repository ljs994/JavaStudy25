package ch01;

public class DoubleFloatExam {

	public static void main(String[] args) {
		// double타입과 float타입
		
		double var1 = 2.98 ;
		float var2 = 6.57F ; // 뒤에 꼭 F를 붙여야 한다.(대 소문자 상관x 하지만 대문자 사용권장)
		
		System.out.println(var1);
		System.out.println(var2);
		
		double var4 = 0.111222333444555666777888999 ;
		float var5 = 0.123456789123456789F ;
		
		System.out.println(var4);
		System.out.println(var5);
		

	}

}
