package ch01;

public class IntExam {

	public static void main(String[] args) {
		
		int var1 = 15 ; // 10진수의 값 : 15
		int var2 = 017 ; // 8진수
		int var3 = 0xB ; // 16진수
		int var4 = 0b0110 ; // 2진수
		
		System.out.println("10진수의 값 : " + var1);
		System.out.println("8진수의 값 : " + var2);
		System.out.println("16진수의 값 : " + var3);
		System.out.println("2진수의 값 : " + var4);
		
		

	}

}
