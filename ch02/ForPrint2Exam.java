package ch02;

import java.util.Scanner;

public class ForPrint2Exam { // class 시작

	public static void main(String[] args) { // main 메서드 시작
		// for문을 사용하여 증가값과 감소값
		for(int i=0 , j=100 ; i<=10 && j>=90 ; i++ , j--) {
			System.out.println("i갑의 증가 : " + i + " // j값의 감소 : " + j);
		}
		
				

	} // main 메서드 종료

} // class 시작
